package ai_agent

import (
	"context"
)

// Message AI消息结构
type Message struct {
	Role    string `json:"role"`    // 角色：user, assistant, system
	Content string `json:"content"` // 消息内容
}

// ChatRequest 聊天请求
type ChatRequest struct {
	Model       string    `json:"model"`       // 模型名称
	Messages    []Message `json:"messages"`    // 消息列表
	Temperature float64   `json:"temperature"` // 温度参数
	MaxTokens   int       `json:"max_tokens"`  // 最大token数

	// 流式响应控制
	Stream bool `json:"stream"` // 是否使用流式响应
}

// ChatResponse 聊天响应
type ChatResponse struct {
	ID      string  `json:"id"`      // 响应ID
	Object  string  `json:"object"`  // 对象类型
	Created int64   `json:"created"` // 创建时间戳
	Model   string  `json:"model"`   // 模型名称
	Choices []struct {
		Index   int    `json:"index"`
		Message Message `json:"message"`
	} `json:"choices"`
	Usage struct {
		PromptTokens     int `json:"prompt_tokens"`
		CompletionTokens int `json:"completion_tokens"`
		TotalTokens      int `json:"total_tokens"`
	} `json:"usage"`
}

// CompletionRequest 文本生成请求
type CompletionRequest struct {
	Model       string  `json:"model"`       // 模型名称
	Prompt      string  `json:"prompt"`      // 提示文本
	Temperature float64 `json:"temperature"` // 温度参数
	MaxTokens   int     `json:"max_tokens"`  // 最大token数

	// 流式响应控制
	Stream bool `json:"stream"` // 是否使用流式响应
}

// CompletionResponse 文本生成响应
type CompletionResponse struct {
	ID      string  `json:"id"`      // 响应ID
	Object  string  `json:"object"`  // 对象类型
	Created int64   `json:"created"` // 创建时间戳
	Model   string  `json:"model"`   // 模型名称
	Choices []struct {
		Index   int    `json:"index"`
		Text    string `json:"text"`
	} `json:"choices"`
	Usage struct {
		PromptTokens     int `json:"prompt_tokens"`
		CompletionTokens int `json:"completion_tokens"`
		TotalTokens      int `json:"total_tokens"`
	} `json:"usage"`
}

// EmbeddingRequest 嵌入向量请求
type EmbeddingRequest struct {
	Model string   `json:"model"` // 模型名称
	Input []string `json:"input"` // 输入文本列表
}

// EmbeddingResponse 嵌入向量响应
type EmbeddingResponse struct {
	ID      string  `json:"id"`      // 响应ID
	Object  string  `json:"object"`  // 对象类型
	Created int64   `json:"created"` // 创建时间戳
	Model   string  `json:"model"`   // 模型名称
	Data    []struct {
		Index     int       `json:"index"`
		Embedding []float64 `json:"embedding"`
		Object    string    `json:"object"`
	} `json:"data"`
	Usage struct {
		PromptTokens int `json:"prompt_tokens"`
		TotalTokens  int `json:"total_tokens"`
	} `json:"usage"`
}

// AIAgent 接口定义
// 所有AI Agent实现都需要实现此接口
// 用于标准化AI服务的调用方式
// 支持聊天、文本生成和嵌入向量等功能
// 同时支持同步和异步调用模式
// 支持流式响应
// 支持批量处理
// 支持缓存机制
type AIAgent interface {
	// 初始化AI Agent
	Init(config map[string]interface{}) error
	
	// 清理资源
	Close() error
	
	// 获取AI Agent名称
	Name() string
	
	// 获取AI Agent版本
	Version() string
	
	// 聊天功能
	Chat(ctx context.Context, req ChatRequest) (*ChatResponse, error)
	
	// 流式聊天功能
	ChatStream(ctx context.Context, req ChatRequest) (<-chan *ChatResponse, <-chan error)
	
	// 文本生成功能
	Completion(ctx context.Context, req CompletionRequest) (*CompletionResponse, error)
	
	// 流式文本生成功能
	CompletionStream(ctx context.Context, req CompletionRequest) (<-chan *CompletionResponse, <-chan error)
	
	// 嵌入向量生成功能
	Embedding(ctx context.Context, req EmbeddingRequest) (*EmbeddingResponse, error)
	
	// 批量嵌入向量生成功能
	BatchEmbedding(ctx context.Context, req []EmbeddingRequest) ([]*EmbeddingResponse, error)
	
	// 模型列表查询功能
	ListModels(ctx context.Context) ([]string, error)
	
	// 模型详情查询功能
	GetModel(ctx context.Context, modelName string) (map[string]interface{}, error)
	
	// 检查AI Agent健康状态
	HealthCheck() error
}

// AIAgentFactory AI Agent工厂接口
// 用于创建AIAgent实例
type AIAgentFactory interface {
	// 创建AIAgent实例
	Create() (AIAgent, error)
	
	// 获取工厂名称
	Name() string
}

// AIAgentManager AI Agent管理器
// 用于管理多个AIAgent实例
type AIAgentManager interface {
	// 注册AI Agent工厂
	RegisterFactory(factory AIAgentFactory) error
	
	// 创建并获取AI Agent实例
	GetAIAgent(name string, config map[string]interface{}) (AIAgent, error)
	
	// 释放AI Agent实例
	ReleaseAIAgent(name string) error
	
	// 列出所有可用的AI Agent名称
	ListAvailableAgents() []string
	
	// 清理所有资源
	Close() error
}

// AIAgentMiddleware AI Agent中间件接口
// 用于在AI Agent调用前后添加额外的处理逻辑
type AIAgentMiddleware interface {
	// 执行中间件逻辑
	Process(ctx context.Context, req interface{}, next func(context.Context, interface{}) (interface{}, error)) (interface{}, error)
	
	// 获取中间件名称
	Name() string
}
